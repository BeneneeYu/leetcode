package dataStructure.hash.question705;

import java.util.Iterator;
import java.util.LinkedList;

/**
 * @program: Leetcode
 * @description: Design a HashSet without using any built-in hash table libraries.
 * @author: Shen Zhengyu
 * @create: 2022-04-21 09:46
 **/
public class MyHashSet {
    private static final int BASE = 769;
    private LinkedList[] data;

    // hash key indicates the position of a key
    // key is the actual data to be stored
    public MyHashSet() {
        data = new LinkedList[BASE];
        for (int i = 0; i < BASE; i++) {
            data[i] = new LinkedList<Integer>();
        }
    }

    public void add(int key) {
        int h = hash(key);
        Iterator<Integer> iterator = data[h].iterator();
        while (iterator.hasNext()) {
            Integer element = iterator.next();
            if (element == key) {
                return;
            }
        }
        data[h].offerLast(key);
    }

    public void remove(int key) {
        int h = hash(key);
        Iterator<Integer> iterator = data[h].iterator();
        while (iterator.hasNext()) {
            Integer element = iterator.next();
            if (element == key) {
                data[h].remove(element);
                return;
            }
        }

    }

    public boolean contains(int key) {
        int h = hash(key);
        Iterator<Integer> iterator = data[h].iterator();
        while (iterator.hasNext()) {
            Integer element = iterator.next();
            if (element == key) {
                return true;
            }
        }
        return false;
    }

    private int hash(int key) {
        return key % BASE;
    }
}

